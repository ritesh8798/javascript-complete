console.log(Math.round(4.6)) //math.round returns the nearest integer
console.log(Math.round(4.3))
console.log(Math.ceil(4.3)) //math.ceil returns the value of x rounded up to its nearest integer
console.log(Math.floor(4.3))//math.floor returns the value of x rounded down to its nearest integer

//math.trunc returns the integer part 

console.log(Math.trunc(45.326))
console.log(Math.trunc(49.823))
console.log(Math.trunc(-31.823))

//math.pow(x,y) returns x to the power y

console.log(Math.pow(5,3))
console.log(Math.pow(2,5))
console.log(Math.pow(10,3))

//math.sqrt() returns square root of the number

console.log(Math.sqrt(100))
console.log(Math.sqrt(625))

